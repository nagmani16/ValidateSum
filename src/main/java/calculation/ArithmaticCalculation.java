package calculation;

public class ArithmaticCalculation {
	
	public ArithmaticCalculation() {
		
	}
	
	public int getSum(int a, int b){
		return a+b;
		
	}
	
	public int getDiff(int a, int b) {
		return a-b;
	}
	
	public int getProduct(int a, int b) {
		return a*b;
	}
	
	public int getDivision(int a, int b) {
		return a/b;
	}

}
